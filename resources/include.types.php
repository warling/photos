function isNonEmptyString( $string )
{
	//	Note that we can't simply use the isEmpty() function because
	//	this will return true if the string is equal to "0".

	if ( isNotString( $string ) ) return false;

	if ( $string === null ) return false;
	if ( $string === emptyString ) return false;

	return true;
}

