date_default_timezone_set( 'America/Chicago' );

////////////////////////////////////////////////////////////////////////////////

define( 'timestampFormat', 'Y-m-d H:i:s' );

////////////////////////////////////////////////////////////////////////////////

function timestampString( $timestamp )
{
	assert( '$timestamp !== null' );
	assert( 'is_int( $timestamp )' );

	return date( timestampFormat, $timestamp );
}

////////////////////////////////////////////////////////////////////////////////

function dateString( $timestamp )
{
	assert( '$timestamp !== null' );
	assert( 'is_string( $timestamp )' );
	assert( '$timestamp !== emptyString' );

	return substr( $timestamp, 0, min( strlen( $timestamp ), 10 ) );
}

////////////////////////////////////////////////////////////////////////////////

function yearString( $timestamp )
{
	assert( '$timestamp !== null' );
	assert( 'is_string( $timestamp )' );
	assert( '$timestamp !== emptyString' );

	return substr( $timestamp, 0, min( strlen( $timestamp ), 4 ) );
}

